import java.util.*;



public class Solution{
    public static void main(String[] args){
     Scanner sc=new Scanner(System.in);
     
     int j=sc.nextInt();
     for(int i=1;i<=10;i++){
       int result=j*i;
       System.out.println(j+" x "+i+" = "+result); 
         
     }
    sc.close();
    
    }
}
